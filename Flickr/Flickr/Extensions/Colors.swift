import UIKit

extension UIColor {
    class var superPink: UIColor {
        return UIColor(red: 166.0/255.0, green: 15.0/255.0, blue: 147.0/255.0, alpha: 1.0)
    }
    
    class var deepBlue: UIColor {
        return UIColor(red: 41.0/255.0, green: 97.0/255.0, blue: 212.0/255.0, alpha: 1.0)
    }
}
